<h4>Stay at The Study Centre</h4><br><br>
<p>The Study Centre has nine cottages for visitors, having a total capacity of housing eighteen persons at a time. The cottages are furnished with cots and bedding, cupboards, tables and chairs. A mosquito net is provided for each bed.</p>


<p>Bathrooms and toilets are attached to the bedrooms. Bathrooms are fitted with electric geysers for hot water. Wholesome and pure vegetarian meals are served at breakfast, lunch, tiffin and dinner.</p>

<p>Facilities of telephone booth, post-office, tuck-shop, laundry and medical aid are also available on the campus. Books, audio and video tapes of Krishnamurti are available for sale.</p>

<p>There is no fixed routine. One may spend time watching the surrounding hills, the still waters of the river Bhima or listen to the sound of the wind blowing over the hills, as one may also watch the stream of thought nonverbally or listen to Krishnamurti's talks and writings and hold informal discussions with others. Physical activities of a constructive and healthy nature could be created and provided in the area of gardening and kitchen.</p>

<p>To maintain the ethos and the atmosphere of the place, smoking, alcoholic drinks, non-vegetarian food items and narcotic drugs are strictly prohibited on the campus.</p>

