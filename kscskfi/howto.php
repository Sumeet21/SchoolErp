<h4>How to reach The Study Centre </h4><br><br>

<p><font style="color:darkblue;"><b>A. By Public Transport from Pune :</font></b><br>

    1. If you are arriving at Pune Railway Station, Please enquire the location of PMT (Pune Municipal Transport) / PCMT (Pimpri Chinchwad Municipal Transport) Bus-Stop. From there take the bus going to Rajgurunagar (Khed).<br>

    2. Most of the local persons at Rajgurunagar know Sahyadri School. There you may take a private jeep or a bus going to village Wada from Rajgurunagar. They will take you up to the foot of the hill. Walking to the top of the hill with your luggage may be difficult. However the jeep may take you to the top of the hill on payment of some extra charges.<br>

    3. If you are arriving at Shivaji Nagar Railway Station or any other part of Pune City, please come to the Shivaji Nagar Bus Stand and take a ST (State Transport) bus going to Rajgurunagar (Khed). At Khed follow instructions 2 and 3 given above. </p>


<p><font style="color:darkblue;"><b>B. By your own vehicle or hired taxi from Pune :</font></b><br>

    1. Take the old Pune-Mumbai Road to come to Nasik-Phata. You will pass through Khadki, Dapodi, Bopodi, Kasarwadi suburbs.</br>
    2. At Nasik-Phata come on the Pune-Nasik Highway and drive towards Nasik. You will cross Pimpri, Bhosari, Moshi and Chakan and come to Rajgurunagar (Khed).</br>
    3. As you cross the Rajgurunagar Bus Stand on your right (just after passing by the bus stand), take a left turn and come on the Wada Road. Sahyadri School is 27 kms away from Rajgurunagar.</br>
    4. Drive towards the village Wada. The Tiwai Hill is 4 km before Wada on this road. There is a signpost approximately 26 km away from Rajgurunagar reading 'The Sahyadri School', 'Krishnamurti Foundation India' at the foot of the Tiwai Hill. Here you have to take a left turn and drive about 1.5 km to climb the hill and come to the study centre.</p>
   
<p><font style="color:darkblue;"><b>C. By your own vehicle or hired taxi from Mumbai :</font></b><br>

    1. Drive on the Mumbai-Pune Express Highway towards Pune. Pay the toll tax at the toll plaza and continue to drive.</br>
    2. After crossing Lonavala, continue to drive on the Express Highway till you come to another toll plaza.</br>
    3. As the toll plaza becomes visible keep your vehicle in the extreme left lane and take the exit marked as Chakan-Talegaon.</br>
    4. Drive towards Talegaon. At Talegaon, take the Talegaonâ€“Chakan Road and come to Chakan. At Chakan, The Talegaon-Chakan road crossses the Pune-Nasik highway.</br>
    5. At Chakan come on the Pune-Nasik Highway and drive towards Nasik to come to Rajgurunagar (Khed).</br>
    6. As you cross the Rajgurunagar Bus Stand on your right (just after passing by the bus stand), take a left turn and come on the Wada Road. Sahyadri School is 27 kms away from Rajgurunagar.</br>
    7. Drive towards the village Wada. The Tiwai Hill is 4 km before Wada on this road. There is a signpost approximately 26 km away from Rajgurunagar reading 'The Sahyadri School', 'Krishnamurti Foundation India' at the foot of the Tiwai Hill. Here you have to take a left turn and drive about 1.5 km to climb the hill and come to the study centre.</p>
    

<p><font style="color:darkblue;"><b>D. By your own vehicle or hired taxi or by public transport from Nasik :</font></b><br>
    1. All the public transport buses and other vehicles travelling from Nasik to Pune pass through Rajgurunagar. So if you are travelling in the public transport vehicle leave the vehicle at Rajgurunagar and follow the instructions 2 and 3 in A.<br>
    2. If you are travelling in your own vehicle or a hired taxi, on reaching Rajgurunagar come to the Wada road and follow the instructions 4 and 5 in B. When you travel from Nasik to Rajgurunagar, the Wada road will be on your right just before you reach the Rajgurunagar bus stand.</p>

