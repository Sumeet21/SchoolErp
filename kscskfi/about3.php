<h4>Travel</h4><br><br>
<p>The Krishnamurti Study Centre Sahyadri is situated on a plateau on the top of a hill about 70 km away from the city of Pune, off Pune-Nasik Road, in the Bhima River Valley.</p>

<p>The campus, which is common to both the Sahyadri School and the Study Centre is 26 km from Rajgurunagar (Khed) off the Pune-Nasik Road. Rajgurunagar is connected by buses, taxis and other means of public transport to and from Pune which is linked by air directly to Mumbai, Chennai, Bangalore, Delhi and other cities. Pune is also linked to all the major cities of India by railway.</p>

<p>When you come to Rajgurunagar, follow the road to the village called Wada. The Krishnamurti Study Centre is about 4 km before Wada and is located on the top of Tiwai Hill 1 KM after the villege Gundalwadi. Signposts for the Sahyadri School have been put at two locations along this road.</p>

<p>Arrangements for pick-up from Pune or Rajgurunagar can be made for the visitors on payment and with sufficient advance notice.</p>

<p>Except for mid-April to mid-June and sometimes October, climate at Sahyadri is moderate with day temperatures around 30°c. During summer the day temperatures could be around 38°C. A good breeze turning sometimes into strong winds always keeps blowing. It is therefore advisable to keep one or two light warm clothes handy during your stay at the Study Centre.</p>

