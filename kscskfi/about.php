        <p><b>L</b>ocated in the picturesque valley of the Bhima River the Krishnamurti Study Centre is an integral part of the Pune Education Centre of K.F.I. It is built on a 140m hilltop plateau of the gently sloping Tiwai hill, 25 km as the bird flies from Bhima Shankar, the source of the Bhima River, in a remote hamlet in interior Maharashtra.</p>

<p>A winding road takes one up to the Study Centre. In the distances are the Sahyadri Mountains. The landscape and the climate make for an ideal place for retreat and inward exploration.</p>
 

<p>The Study Centre has well-furnished cottages for visitors to stay. There is a separate dining hall where one can take quiet meals.</p>

<p>The main Study Centre building has a unique architectural design. Shaped like a twelve-petal lotus, the building exudes serenity and a special atmosphere. A spacious hall of twelve sides surrounded by a wide veranda on all sides is an ideal location for deep study. The hall serves as the main library, audio-video viewing room and the silence room.</p>

<p>The library contains books of Krishnamurti and audio-videotapes of his talks. There is a facility for visitors to hear audiotapes and view videotapes of Krishnamurti's talks and discussions. Also a computer terminal to access the complete published works of Krishnamurti is available.</p>

