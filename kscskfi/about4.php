<p>The charges for stay at the Study Centre are Rs. 600/- per day per person for persons earning in India and US $20/- per day per person for those earning abroad. However if the room is shared with another person, the corresponding charges are Rs. 400/- and US $15/- per day per person. Concessions can be made for persons who are unable to pay the charges at the usual rate. The charges cover only lodging and boarding. The other facilities like telephone booth, post-office, laundry, tuck-shop, transportation, etc. are to be paid separately. There are no separate charges for the use of the library, audio/video and other facilities of study.</p>

