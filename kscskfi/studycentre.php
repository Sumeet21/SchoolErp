<h4>The Study Centre</h4><br><br>

<p>(The Study) is a place for the flowering of goodness; a communication and cooperation not based on work, ideal or personal authority; but cooperation implies not round some subject or principle, belief and so on. As one comes to the place, each one in his work - working in the garden or doing something - may discover something as he is working, and he communicates and has a dialogue with the other inhabitants - to be questioned, doubted and to see the weight of the truth of his discovery. So there is a constant communication and not a solitary achievement, solitary enlightenment or understanding. It is the responsibility of each one to bring this about. Each one of us, if he discovers something basic, new - it is not personal but it is for all the people who are there...</p>

<p>A dialogue is very important. It is a form of communication in which question and answer 
continues until a question is left without an answer. Thus the question is suspended between two 
persons involved in this answer and question. It is like a bud which untouched blossoms ... If the 
question is left totally untouched by thought, it then has its own answer because the questioner and 
answerer, as persons, have disappeared. This is a form of dialogue in which investigation reaches a 
certain point of intensity and depth, which then has a quality which thought can never reach. It is 
not a dialectical investigation of opinions, ideas, but rather exploration by two or many serious, 
good brains.</p> 

<p>This place must be of great beauty with trees, birds and quietness, for beauty is truth and truth is 
goodness and love. The external beauty, external tranquility, silence, may affect the inner 
tranquility, but the environment must in no way influence the inner beauty. Beauty can only be 
when the self is not; the environment, which must have great wonder, must in no way be an 
absorbing factor, like a toy is with a child. Here, there are no toys but inner depth, substance and 
integrity that is not put together by thought...</p> 

<p><b>- From the statement dictated by Krishnamurti to a Trustee of the Foundation, at Vasanta Vihar on 26-27 January 1984</b></p>

